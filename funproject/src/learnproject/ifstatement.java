package learnproject;

public class ifstatement {
	public static void main(String[] args) {
		
		int x = 30;
		int y = 40;
		
		if (x<y) {
			System.out.println("x is greater than y");
		}else if (x==y) {
			System.out.println("x is equal than y");
		}else if (x>y) {
			System.out.println("x is less than y");
		}
	}
}
